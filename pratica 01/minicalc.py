numero1 = input("Digite o primeiro número: ")
numero2 = input("Digite o segundo número: ")

soma = int(numero1) + int(numero2)
multiplicacao = int(numero1) * int(numero2)
subtracao = int(numero1) - int(numero2)
divisao = int(numero1) / int(numero2)


print("A soma dos números é:", soma)
print("A multiplicação dos números é:", multiplicacao)
print("A subtração dos números é:", subtracao)
print("A divisão dos números é:", divisao)
print("A média dos números é:", soma / 2)